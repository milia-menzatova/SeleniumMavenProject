package org.example;

public class booleanData {
    public static void main(String[] args) {
       boolean user = true;
       if (!user == true){
           System.out.println("it's true");
       }
       else if (!user){
           System.out.println("it's not user");
       }
      else {
          System.out.println("it's false");

       }
    }
}

